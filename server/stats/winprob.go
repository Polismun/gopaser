// GetWinProbCT returns P(CT wins) given alive counts and bomb status.
func GetWinProbCT(ctAlive, tAlive int, bombPlanted bool) float64 {
	bomb := 0
	if bombPlanted {
		bomb = 1
	}
	key := fmt.Sprintf("%d-%d-%d", ctAlive, tAlive, bomb)
	if p, ok := winProbTable[key]; ok {
		return p
	}
	return 0.5
}
